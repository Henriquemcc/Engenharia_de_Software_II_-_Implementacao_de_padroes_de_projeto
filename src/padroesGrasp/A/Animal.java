/**
 * Representa um animal.
 */
public interface Animal {

    /**
     * Obtém o nome da espécie do animal.
     *
     * @return Nome da espécie do animal.
     */
    String getNomeEspecie();

    /**
     * Obtém o nome do animal.
     *
     * @return Nome do animal.
     */
    String getNomeAnimal();
}
